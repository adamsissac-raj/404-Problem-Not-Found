package Chapter_5;

public class week_and_days 
{

	public static void main(String[] args) 
	{
		for(int i=1;i<=12;i++)
		{
			System.out.println("Week "+i);
			for(int j=1;j<=5;j++)
			{
				System.out.println("Day "+j);
			}
			System.out.println("");
		}
		System.out.println("Completed");

	}

}
